package  main.java.member.model.vo;

public class ClassKeyword {
	private String classKeyword;  //CLASS_KEYWORD
	
	public ClassKeyword() {
		super();
	}

	public ClassKeyword(String classKeyword) {
		super();
		this.classKeyword = classKeyword;
	}

	public String getClassKeyword() {
		return classKeyword;
	}

	public void setClassKeyword(String classKeyword) {
		this.classKeyword = classKeyword;
	}

	@Override
	public String toString() {
		return "ClassKeyword [classKeyword=" + classKeyword + "]";
	}
	
	

}
